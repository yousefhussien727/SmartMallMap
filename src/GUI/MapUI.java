    private void infoBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_infoBtnMouseClicked
        String fn = FacilityLabel.getText();
        if(fn.equals("Select a Place"))
            JOptionPane.showMessageDialog(this, "Select a Place from the map First", "Error Message", ERROR_MESSAGE);
        else if(fn.equals("X"))
            JOptionPane.showMessageDialog(this, "Sorry! This place is currently unavailable", "Error Message", ERROR_MESSAGE);
        else
        {
            InfoDialog.setVisible(true);
            InfoDialog.setSize(400, 380);
            InfoDialog.setLocationRelativeTo(this);
                
            facilityInfoLabel.setText(fn);
            category.setText(mapm.getFacilityByName(fn).getCategory());
            phoneNum.setText(mapm.getFacilityByName(fn).getPhoneNo());
            workFrom.setText(String.valueOf(mapm.getFacilityByName(fn).getWorkingHours()[0]));
            workTo.setText(String.valueOf(mapm.getFacilityByName(fn).getWorkingHours()[1]));
            description.setText("<html><p>" + mapm.getFacilityByName(fn).getDescription() + "</p></html>");
        }
    }//GEN-LAST:event_infoBtnMouseClicked

