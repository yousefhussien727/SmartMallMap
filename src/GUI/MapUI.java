    private void sendBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendBtnMouseClicked
        String desc = (String)inqDescription.getText();
        String type = (String)inqTypeBox.getSelectedItem();
        
        if (type.equals(null) || type.equals(""))
            JOptionPane.showMessageDialog(this, "Please, Choose the Type of the Inquiry!", "Invalid Input", ERROR_MESSAGE);
        else if (desc.equals("Write your Inquiry here") || desc.equals(""))
            JOptionPane.showMessageDialog(this, "Please, Enter the Inquiry!", "Invalid Input", ERROR_MESSAGE);
        else
        {
            mapm.sendInquiry(type, desc);
            JOptionPane.showMessageDialog(this, "Your Inquiry is sent. Thank you for Reaching us!", "Confirmation", INFORMATION_MESSAGE);
            SupportDialog.dispose();
        }
    }//GEN-LAST:event_sendBtnMouseClicked

