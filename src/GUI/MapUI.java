    private void dirBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dirBtnMouseClicked
        String fn = FacilityLabel.getText();
        if(fn.equals("Select a Place"))
            JOptionPane.showMessageDialog(this, "Select a Place from the map First", "Error Message", ERROR_MESSAGE);
        else if(fn.equals("X"))
            JOptionPane.showMessageDialog(this, "Sorry! This place is currently unavailable", "Error Message", ERROR_MESSAGE);
        else
        {
            Graphics g = this.getGraphics();
            paint(g);
            g.setColor(Color.red);
            int[][] d = mapm.getDirection(fn);
            for(int i = 1; i <= d[0][0]; i++)
                g.drawLine(d[i][0], d[i][1], d[i+1][0], d[i+1][1]);
        }       
    }//GEN-LAST:event_dirBtnMouseClicked
